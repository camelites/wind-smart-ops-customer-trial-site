function q(prompt, acceptedAnswers, explanation, knowledgePoint, checkMode = "exact") {
  return { prompt, acceptedAnswers, checkMode, explanation, knowledgePoint };
}

function unitPractice(...exampleFamilies) {
  return {
    exampleFamilies,
    examplePattern: exampleFamilies.map((family) => `${family.title}：${family.method}`).join("；"),
    practiceQuestions: exampleFamilies.flatMap((family) => family.questions.map((question) => ({
      ...question,
      exampleFamily: family.title,
      sourceConnection: family.sourceConnection
    })))
  };
}

